    public static void main(String[] args) {
        
        //Autoboxing =  is converting primitive data types to classes
        //              used incase there is a need for objects eg when dealing with arraylists

        //convert an int primitive data type to a class
        Integer a = 123;

        //create a class of a primitive data type double
        Double b = 3.14;


        //Character
        Character c = '@';

        //boolean
        Boolean d = true;

        //By wrapping these primitive datatypes in objects. We can use them in collections frameworks such as arraylists
    }
